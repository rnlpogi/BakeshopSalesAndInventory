    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed

        String name = (pname.getText());
        String category = (cat.getText());
       int expenses = Integer.parseInt(exp.getText());
        int prices = Integer.parseInt(price.getText());
        int netincome = Integer.parseInt(net.getText());
        int quantity = Integer.parseInt(qty.getText());
    
        int expe = expenses * quantity;
        int tprice = quantity * prices;
        int neti = netincome * quantity;
         int total = quantity * tprice;

        model = (DefaultTableModel) summary.getModel();
        model.addRow(new Object[]{
            name,
          category,
            expe,
            tprice,
            neti,
            quantity,
            
            total});
        int sum = 0;
        for (int i = 0; i < summary.getRowCount(); i++) {
            sum = sum + Integer.parseInt(summary.getValueAt(i, 6).toString());
        }
        txttotalcost.setText(Integer.toString(sum));
        
        retrievestocks();
        id.setText("");
        pname.setText("");
        cat.setText("");
       exp.setText("");
       price.setText("");
        net.setText("");
        qty.setText("");
    }//GEN-LAST:event_jButton8ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
    model.removeRow(summary.getSelectedRow());
        int sum = 0;
        for (int e = 0; e < summary.getRowCount(); e++) {
            sum = sum + Integer.parseInt(summary.getValueAt(e, 6).toString());
        }
        
        txttotalcost.setText(Integer.toString(sum));
        if (txtcash.getText().isEmpty()) {
            txtchange.setText("");
        } else {
            int total = Integer.parseInt(txttotalcost.getText());

            int pay = Integer.parseInt(txtcash.getText());
            int change = pay - total;
            txtchange.setText(String.valueOf(change));
        }
        retrievestocks();
      id.setText("");
        pname.setText("");
        cat.setText("");
       exp.setText("");
       price.setText("");
        net.setText("");
        qty.setText("");
      
    }//GEN-LAST:event_jButton6ActionPerformed

