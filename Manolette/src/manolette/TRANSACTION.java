private void save_data() {
        model = (DefaultTableModel) summary.getModel();
        String productn, category, expenses, prices, net_income,quantity, totalcost;
        try {
            for (int i = 0; i < summary.getRowCount(); i++) {
                productn = model.getValueAt(i, 0).toString();
                category = model.getValueAt(i, 1).toString();
                expenses = model.getValueAt(i, 2).toString();
                prices = model.getValueAt(i, 3).toString();
                net_income = model.getValueAt(i, 4).toString();
                quantity = model.getValueAt(i, 5).toString();
                totalcost = model.getValueAt(i, 6).toString();

                String sql = "INSERT INTO  transaction(p_name,p_category,p_expenses,p_prices,p_netincome,p_quantity,totalcost)VALUE(?,?,?,?,?,?,?)";
                pst = conn.prepareStatement(sql);
                pst.setString(1, productn);
                pst.setString(2, category);
                pst.setString(3, expenses);
                pst.setString(4, prices);
                pst.setString(5, net_income);
                pst.setString(6, quantity);
                pst.setString(7, totalcost);

                pst.execute();

            }
            JOptionPane.showMessageDialog(null, "Transaction Successful");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

    
private void retrievestocks() {
    try {
        String sql = "SELECT product_id AS 'PRODUCT ID', p_name AS 'Product Name', p_category AS 'Category', p_expenses AS 'Expenses', p_prices AS 'PRICE', (p_prices - p_expenses) AS 'Net Income' FROM product_details";
        pst = conn.prepareStatement(sql);
        rs = pst.executeQuery();
        manage.setModel(DbUtils.resultSetToTableModel(rs));
        manage.removeColumn(manage.getColumnModel().getColumn(0));
    } catch (Exception e) {
        JOptionPane.showMessageDialog(rootPane, e);
    }
}
